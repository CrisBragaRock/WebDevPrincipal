function resize() {
    document.getElementById('size').innerHTML = '<h1>Tamanho da janela alterado!</h1>'
    document.getElementById('size').style.backgroundColor = 'lightgray'
}

function rolar() {
    document.getElementById('scroll').style.backgroundColor = 'lightgreen'
    document.getElementById('scroll').innerHTML = '<h1>Rolagem acionada!</h1>'
}