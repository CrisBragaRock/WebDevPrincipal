var lista_frutas = Array()

lista_frutas[0] = 'Banana'
lista_frutas[1] = 'Maçã'

console.log(lista_frutas)

//incluir elementos no final do array
lista_frutas.push('Uva')
//incluir elementos no inicio do array
lista_frutas.unshift('Amora')

console.log(lista_frutas)

//excluir elementos do final do array
lista_frutas.pop()
//excluir elementos do inicio do array
lista_frutas.shift()

console.log(lista_frutas)